import 'package:flutter/cupertino.dart';
import 'package:screen_protector/screen_protector.dart';

class ScreenShotProtector {
 static Future<void> enableScreenProtection() async {
    try {
      await ScreenProtector.preventScreenshotOn();
      debugPrint('Screenshot protection enabled ');
    } catch (e) {
      debugPrint('Failed to enable screenshot protection: $e');
    }
  }
 static Future<void> disableScreenProtection() async {
    try {
      await ScreenProtector.preventScreenshotOff();
      debugPrint('Screenshot protection disabled ');
    } catch (e) {
      debugPrint('Failed to enable screenshot protection: $e');
    }
  }
}

